#!/usr/bin/env node
/**
 * verify-linkedin-cloud.mjs — Antigravity LinkedIn Cloud Verification
 *
 * One command. One receipt. No live publishing.
 *
 * PASS criteria (ALL must be true):
 *   configured === true
 *   disabled   === false
 *   validated  === true
 *   dryRun     === true
 *   success    === false
 *
 * Usage:
 *   node scripts/verify-linkedin-cloud.mjs
 *   just linkedin-verify-cloud
 *
 * Output:
 *   artifacts/receipts/ag-linkedin-dryrun-YYYYMMDD-HHMM.md
 */

import { writeFileSync, mkdirSync } from 'fs';
import { join } from 'path';

const CLOUD_BASE = 'https://sirtrav-a2a-studio.netlify.app/.netlify/functions';
const RECEIPTS_DIR = join(process.cwd(), 'artifacts', 'receipts');

// ─── Timestamp helpers ───
const now = new Date();
const pad = (n) => String(n).padStart(2, '0');
const ts = `${now.getFullYear()}${pad(now.getMonth() + 1)}${pad(now.getDate())}-${pad(now.getHours())}${pad(now.getMinutes())}`;
const iso = now.toISOString();

// ─── Dry-run payload ───
const payload = {
  projectId: 'sirtrav-test',
  runId: `linkedin-verify-${Date.now()}`,
  videoUrl: 'https://sirtrav-a2a-studio.netlify.app/test-assets/sample.mp4',
  title: 'SirTrav dry-run smoke',
  description: 'SirTrav dry-run smoke (LinkedIn)',
  visibility: 'PUBLIC',
  dryRun: true,
};

// ─── Pass/Fail evaluation ───
function evaluate(json, httpStatus) {
  const checks = {
    configured: json.configured === true,
    not_disabled: json.disabled === false || json.disabled === undefined,
    validated: json.validated === true,
    dryRun: json.dryRun === true,
    success_false: json.success === false,
    http_ok: httpStatus >= 200 && httpStatus < 300,
  };

  // Detect disabled state (different from fail)
  if (json.disabled === true) {
    return {
      verdict: 'FAIL',
      reason: `disabled=true — ${json.reason || json.error || json.note || 'keys not configured in Netlify'}`,
      checks,
    };
  }

  const allPass = checks.configured && checks.not_disabled && checks.validated && checks.dryRun && checks.success_false;

  if (allPass) {
    return {
      verdict: 'PASS',
      reason: 'All dry-run criteria met. Live fire withheld correctly.',
      checks,
    };
  }

  // Build specific failure reasons
  const failures = [];
  if (!checks.configured) failures.push('configured !== true');
  if (!checks.not_disabled) failures.push('disabled === true');
  if (!checks.validated) failures.push('validated !== true');
  if (!checks.dryRun) failures.push('dryRun !== true');
  if (!checks.success_false) failures.push(`success === ${json.success} (expected false in dry-run)`);
  if (!checks.http_ok) failures.push(`HTTP ${httpStatus}`);

  return {
    verdict: 'FAIL',
    reason: failures.join('; '),
    checks,
  };
}

// ─── Build receipt markdown ───
function buildReceipt(verdict, reason, checks, json, httpStatus, error) {
  const checkMark = (v) => v ? '  PASS' : '  FAIL';

  if (error) {
    return `# Antigravity — LinkedIn Cloud Verification Receipt
**Date:** ${iso}
**Agent:** Antigravity (QA only)
**Scope:** Dry-run verification. No live publishing authorized.

## STATUS: ERROR

**Verdict:** Could not reach LinkedIn cloud publisher.

## EVIDENCE

- Endpoint: \`${CLOUD_BASE}/publish-linkedin\`
- Error: \`${error}\`

## INTERPRETATION

The cloud function did not respond. This could mean:
- Netlify deploy is down or in progress
- Function crashed on invocation
- Network issue

## RISKS

- Cannot verify publisher state
- Cannot confirm key presence

## NEXT STEP

Check \`just healthcheck-cloud\` and verify Netlify deploy status.

---
*Generated by \`just linkedin-verify-cloud\` — For the Commons Good*
`;
  }

  const operational = verdict === 'PASS' ? 'HOLD for human-approved live post' : 'NOT READY';

  return `# Antigravity — LinkedIn Cloud Verification Receipt
**Date:** ${iso}
**Agent:** Antigravity (QA only)
**Scope:** Dry-run verification. No live publishing authorized.

## STATUS: ${verdict}

**QA Verdict:** ${verdict} — ${reason}
**Operational State:** ${operational}

## EVIDENCE

| Check | Expected | Actual | Result |
|-------|----------|--------|--------|
| configured | true | ${json.configured} |${checkMark(checks.configured)} |
| disabled | false | ${json.disabled} |${checkMark(checks.not_disabled)} |
| validated | true | ${json.validated} |${checkMark(checks.validated)} |
| dryRun | true | ${json.dryRun} |${checkMark(checks.dryRun)} |
| success | false | ${json.success} |${checkMark(checks.success_false)} |
| HTTP status | 2xx | ${httpStatus} |${checkMark(checks.http_ok)} |

## RAW RESPONSE

\`\`\`json
${JSON.stringify(json, null, 2)}
\`\`\`

## INTERPRETATION

${verdict === 'PASS'
    ? 'This is the correct No-Fake-Success outcome. The LinkedIn publisher is wired in cloud, accepts the payload, and truthfully withholds live publish during dry-run. success=false in dry-run means live publish was intentionally withheld — this is expected and is a QA PASS, not a functional failure.'
    : `The dry-run did not meet all PASS criteria. Reason: ${reason}`}

## RISKS

- Live publish path has not been executed in this verification
- Access token freshness is not proven by dry-run alone
- Final text, media URL, and target account still require human approval

## NEXT STEP

${verdict === 'PASS'
    ? 'Hold position. Do not run linkedin-live unless Human-Ops explicitly approves the exact live payload and target account with: APPROVED FOR LIVE LINKEDIN POST'
    : 'Investigate the failure. Check Netlify Dashboard for key presence, then re-run this verification.'}

---
*Generated by \`just linkedin-verify-cloud\` — For the Commons Good*
`;
}

// ─── Main ───
async function main() {
  console.log('🔍 LinkedIn Cloud Verification — Dry-Run Only');
  console.log('   Endpoint:', `${CLOUD_BASE}/publish-linkedin`);
  console.log('   Mode: dryRun=true (no live post)');
  console.log();

  let json, httpStatus, error;

  try {
    const res = await fetch(`${CLOUD_BASE}/publish-linkedin`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify(payload),
    });

    httpStatus = res.status;

    try {
      json = await res.json();
    } catch {
      error = `Non-JSON response (HTTP ${httpStatus})`;
    }
  } catch (err) {
    error = err.message;
  }

  // ─── Evaluate ───
  let verdict, reason, checks;

  if (error) {
    verdict = 'ERROR';
    reason = error;
    checks = {};
  } else {
    const result = evaluate(json, httpStatus);
    verdict = result.verdict;
    reason = result.reason;
    checks = result.checks;
  }

  // ─── Print to console ───
  const icon = verdict === 'PASS' ? '✅' : verdict === 'FAIL' ? '❌' : '⚠️';
  console.log(`${icon} VERDICT: ${verdict}`);
  console.log(`   ${reason}`);

  if (json) {
    console.log();
    console.log('   Evidence:');
    console.log(`     configured = ${json.configured}`);
    console.log(`     disabled   = ${json.disabled}`);
    console.log(`     validated  = ${json.validated}`);
    console.log(`     dryRun     = ${json.dryRun}`);
    console.log(`     success    = ${json.success}`);
  }

  // ─── Write receipt ───
  mkdirSync(RECEIPTS_DIR, { recursive: true });
  const receiptPath = join(RECEIPTS_DIR, `ag-linkedin-dryrun-${ts}.md`);
  const receipt = buildReceipt(verdict, reason, checks, json || {}, httpStatus, error);
  writeFileSync(receiptPath, receipt, 'utf-8');

  console.log();
  console.log(`📋 Receipt written: ${receiptPath}`);

  if (verdict === 'PASS') {
    console.log();
    console.log('   Antigravity verdict: PASS — LinkedIn cloud publisher is operational,');
    console.log('   configured, and honest in dry-run mode; live fire remains blocked');
    console.log('   pending explicit Human-Ops approval.');
  }

  // Exit code: 0 for PASS, 1 for FAIL/ERROR
  process.exitCode = verdict === 'PASS' ? 0 : 1;
}

main();
